            // 请在此处粘贴你的中文文章
            string article = @"核心
启18章审判巴比伦之后，在启19章灵肉将参与羔羊的婚筵。
举行婚筵的地方，就是证据帐幕圣殿，殉教的众灵和证据帐幕圣殿的众圣徒参与到灵肉婚筵，成为蒙福的人。

序论
启19章的题目是《灵肉羔羊的婚筵》。
在启18章里，神亲自用火审判了魔鬼的国度巴比伦，就像启18：23一样，与魔鬼新郎结婚的事情不会再有了，此后，灵肉羔羊的婚筵发生在何处，得到邀请的灵肉又是何人，具备怎样的资格才能参与到这婚筵？就让我们来了解一下本文内容。

1. 
首先，是启19：1-6。
就像1节里“我听见”一样，在启示录成就时，对于应验每章所成就的实状，有这么一位看见、听见并对此加以见证的人。
这位就是新约翰。
1节里的“此后”，是指启18章审判“与魔鬼新郎结了婚的魔鬼的国度巴比伦”的这件事之后。
启18章的巴比伦被审判后，才出现启19章的羔羊的婚筵。
因此，启18章和启19章是相对照的内容，这就像启13章和启14章、启6章和启7章相对照一样。
这时，约翰听见好像群众在天上大声说，这“天”是指何处？从灵意上看，在这地上有两个天，一个是启13章的头一层天即第一帐幕，另一个则是启14-15章的新天即第二层帐幕。
本文1节里的“天”，是指在先前的天地不再有之后而被造的新天即证据帐幕圣殿。
聚集于此的“群众”，是指领悟了应验启示录而成就的实状，走到神面前的人们。
他们说“哈利路亚！救恩、荣耀、权能都属乎我们的神”，这是因为作为羔羊的耶稣与魔鬼争战得胜，把被巴比伦掳掠的百姓拯救了出来。
6节里说“主我们的神、全能者作王了”，这是指因着在启18章审判了魔鬼的国度巴比伦，在亚当犯罪后原本被魔鬼主管的世界变成神作王了。

2. 
